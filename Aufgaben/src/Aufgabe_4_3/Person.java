package Aufgabe_4_3;

public class Person {
    private String name;
    private String vorname;

    public void setName(String n) {
        name = n;
    }

    public String getName() {
        return name;
    }

    public void setVorname(String vn) {
        vorname = vn;
    }

    public String getVorname() {
        return vorname;
    }
}
